package com.model;

public class Blank {
    private int id;//题号
    private String timu;
    private String ans;
    private int chp;
    private int diff;

    public void setId(int id){
        this.id=id;
    }
    public void setTimu(String timu){
        this.timu=timu;
    }
    public void setAns(String ans){
        this.ans=ans;
    }
    public void setChp(int chp){
        this.chp=chp;
    }
    public void setDiff(int diff){
        this.diff=diff;
    }

    public int getId(){
        return id;
    }

    public String getTimu(){
        return timu;
    }
    public String getAns(){
        return ans;
    }
    public int getChp(){
        return chp;
    }
    public int getDiff(){
        return diff;
    }
}
